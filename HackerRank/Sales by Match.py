def sockMerchant(n, ar):
    # Write your code here
    lista = ar.copy()
    pares = 0

    for cor in lista:
        if not ar.count(cor) == 0:
                pares += ar.count(cor)//2
                while cor in ar:
                    ar.remove(cor)
    return pares

